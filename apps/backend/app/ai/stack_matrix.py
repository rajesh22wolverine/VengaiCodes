# ═══════════════════════════════════════════════════════════════
#  VengaiCode — Tech Stack Compatibility Matrix
#  ai/stack_matrix.py — Lets a user pick a UI language+framework, a
#  backend language+framework, and an API style, and tells them
#  definitively whether that combination is possible.
#
#  HONEST SCOPE: "possible" is judged in two layers.
#    1. COHERENCE — does the framework actually run in that language,
#       and does the backend actually support that API style? This is
#       checked exhaustively against every known framework/language/
#       API-style combination (see ALL_COHERENT_COMBOS below) — not a
#       guess.
#    2. BUILDABILITY — of the combinations that ARE coherent, only a
#       small set (BUILDABLE_NOW) can actually be generated by
#       codegen.py today. A coherent-but-unbuilt combo is never
#       silently swapped for React+FastAPI without saying so — see
#       get_project_stack()'s "fallback_reason".
#  Whenever a request isn't buildable (or isn't even coherent), the
#  nearest BUILDABLE combination is suggested, found by scoring every
#  entry in BUILDABLE_NOW — never a heuristic guess.
# ═══════════════════════════════════════════════════════════════

