import io
import logging
import re
import zipfile
from datetime import datetime, timezone
from pathlib import Path

from fastapi import APIRouter, Depends, HTTPException, status
from fastapi.responses import StreamingResponse
from sqlalchemy import select
from sqlalchemy.ext.asyncio import AsyncSession

from app.api.v1.auth import get_current_active_user
from app.core.database import get_db
from app.models.project import AppCategory, Project
from app.models.user import User

logger = logging.getLogger("vengaicode.export")
router = APIRouter()


def safe_filename(name: str) -> str:
    """Sanitize project name for use as a zip filename."""
    cleaned = re.sub(r"[^\w\s-]", "", name).strip()
    cleaned = re.sub(r"[\s]+", "_", cleaned)
    return cleaned[:50] or "vengaicode_project"


def build_export_filename(project_name: str, app_name: str | None = None) -> str:
    """Build a safe ZIP filename using the app name if provided."""
    base_name = (app_name or project_name or "vengaicode_project").strip()
    if not base_name:
        base_name = "vengaicode_project"
    return f"{safe_filename(base_name)}.zip"


def get_repo_root() -> Path:
    return Path(__file__).resolve().parents[4]


def add_template_folder_to_zip(zip_file: zipfile.ZipFile, template_dir: Path, prefix: str) -> None:
    for file_path in template_dir.rglob("*"):
        if file_path.is_file():
            relative_path = file_path.relative_to(template_dir)
            zip_path = Path(prefix) / relative_path
            zip_file.writestr(str(zip_path), file_path.read_text(encoding="utf-8"))


def is_o3de_project(project: Project) -> bool:
    architecture = (project.architecture_data or {}).get("architecture", {})
    frontend = (architecture.get("tech_stack", {}) or {}).get("frontend", "")
    return isinstance(frontend, str) and ("o3de" in frontend.lower() or "open 3d engine" in frontend.lower())


@router.get(
    "/{project_id}/download",
    summary="Download generated code files as a ZIP archive",
)
async def download_project_zip(
    project_id: str,
    include_o3de: bool = False,
    app_name: str | None = None,
    user: User = Depends(get_current_active_user),
    db: AsyncSession = Depends(get_db),
):
    """
    Packages the project's generated code files (from codegen_data)
    into a ZIP archive and streams it back for download.

    This is the first slice of the Export phase — just the raw
    generated files, preserving their folder structure. Later
    sprints will add: documentation (README, FRD, architecture docs),
    packaged installers, and licensing/Tiger stamp injection.
    """
    result = await db.execute(
        select(Project).where(
            Project.id == project_id,
            Project.user_id == user.id,
        )
    )
    project = result.scalar_one_or_none()

    if project is None:
        raise HTTPException(status_code=status.HTTP_404_NOT_FOUND, detail="Project not found.")

    if not project.codegen_data or not project.codegen_data.get("codegen"):
        raise HTTPException(
            status_code=status.HTTP_400_BAD_REQUEST,
            detail="No generated code available to export yet.",
        )

    files = project.codegen_data["codegen"].get("files", [])
    if not files:
        raise HTTPException(
            status_code=status.HTTP_400_BAD_REQUEST,
            detail="No files found in generated code.",
        )

    # Build ZIP in memory
    zip_buffer = io.BytesIO()
    with zipfile.ZipFile(zip_buffer, "w", zipfile.ZIP_DEFLATED) as zip_file:
        # Add a simple README at the root explaining what this is
        readme_content = (
            f"# {project.name}\n\n"
            f"Generated by VengaiCode 🐯 on "
            f"{datetime.now(timezone.utc).strftime('%Y-%m-%d')}\n\n"
            f"{project.codegen_data['codegen'].get('summary', '')}\n\n"
            f"---\n\n"
            f"⚠️  This is a STARTER SKELETON, not a finished application.\n"
            f"Files include model definitions, API route stubs, and basic\n"
            f"component shells based on your approved requirements, UI/UX\n"
            f"design, and architecture. Business logic and styling still\n"
            f"need to be implemented.\n\n"
            f"Files included: {len(files)}\n"
        )
        zip_file.writestr("README.md", readme_content)

        # Add each generated file at its specified path
        for file_data in files:
            path = file_data.get("path", "unnamed_file.txt")
            content = file_data.get("content", "")
            # Prevent path traversal — strip any leading slashes or ../
            safe_path = path.lstrip("/").replace("..", "")
            zip_file.writestr(safe_path, content)

        # Include O3DE template if the project looks like O3DE or the
        # client explicitly requested it via the `include_o3de` query flag.
        if include_o3de or is_o3de_project(project):
            template_root = get_repo_root() / "templates" / "o3de"
            if template_root.exists():
                add_template_folder_to_zip(zip_file, template_root, "o3de_template")

    zip_buffer.seek(0)

    filename = build_export_filename(project.name, app_name)

    logger.info(f"Exported {len(files)} files for project {project_id} as {filename}")

    return StreamingResponse(
        zip_buffer,
        media_type="application/zip",
        headers={
            "Content-Disposition": f'attachment; filename="{filename}"',
        },
    )
